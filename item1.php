	<h4>What is Automatic Speech Recognition (ASR)?</h4>
	<p>Speech is the primary mode of communication among human beings. On the other hand, prevalent means of input to computers is 
	through a keyboard or a mouse. It would be nice if computers could listen to human speech and carry out their commands. Automatic 
	Speech Recognition (ASR) is the process of deriving the transcription (word sequence) of an utterance, given the speech waveform. 
	Speech understanding goes one step further, and gleans the meaning of the utterance in order to carry out the speakerâ€™s command. </p>
	<br>

	<h4>There are many ways to apply ASR to daily life</h4>
	<p>ASR systems facilitate a physically handicapped person to command and control a machine. Even
	ordinary persons would prefer a voice interface over a keyboard or mouse. The advantage is more obvious
	in case of small hand held devices. Dictation machine is a well known application of ASR. Thanks to
	the ubiquitous telecommunication systems, speech interface is very convenient for data entry, access of
	information from remote databases, interactive services such as ticket reservation. ASR systems are
	expedient in cases where hands and eyes are busy such as driving or surgery. They are useful for teaching
	phonetic and programmed teaching as well.</p>
