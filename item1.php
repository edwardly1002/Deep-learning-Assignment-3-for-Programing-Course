	<h4>The world's ASR status</h4>
	<p>There are several Dictation Systems of very large (over 100,000) vocabulary with recognition accuracy of
	about 97%; these are speaker dependent and needs to be trained. Automatic Call Routing systems based
	on ASR technology have been in use for more than a decade. A few airlines employ speech recognition
	systems to provide information to callers and even permit booking of airline ticket. An ASR system
	recognises and understands quries in fluently spoken German, and provides train timetable information. 
	Other example ASR applications are automated switchboard of a large German company, the movie
	or football information system.</p>
	<br>

	<h4>Conclusion</h4>
	<p>Significant progress has been made in the past couple of decades in the area of spoken language technology.
	This has led to deployment of speech recognition systems in a few application domains. Yet, the current
	engineering models of speech and language do not adequately model the natural language capabilities of
	human brain. The cognitive aspects of human brain are complex and development of appropriate models
	is still a challenging research task. Such a development will lead to Ubiquitous Speech Communication
	Interfaces through which people will be able to interact with machines as conveniently and naturally as
	do amongst themselves.
	</p>

